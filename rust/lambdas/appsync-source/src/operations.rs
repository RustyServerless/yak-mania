pub async fn buy_yak(
    player_id: ID,
    secret: String,
    from_place: Option<WaitingPlace>,
    for_job: Job,
) -> Result<GameUpdate, AppsyncError> {
    let current_span = tracing::Span::current();
    current_span.record("job", for_job.to_string());

    let (yak, player, game_status, game_counts) = tokio::try_join!(
        async {
            match from_place {
                Some(from_place) => {
                    Some(WaitingYak::get_random_waiting(dynamodb(), from_place).await).transpose()
                }
                None => Ok(None),
            }
        },
        Player::get(dynamodb(), ItemId::pk(player_id)).send(),
        GameStatus::get(dynamodb(), ItemId::NONE).send(),
        GameCounts::get(dynamodb(), ItemId::NONE).send()
    )
    .map_err(internal_server_error)?;

    let mut player =
        player.ok_or_else(|| trace_appsync_error!("PlayerNotFound", "Player does not exist"))?;
    current_span.record("player_name", &player.name);

    // A player that already have a job should never be able to buy a Yak
    if player.assignment.is_some() {
        return Err(trace_appsync_error!(
            "InvalidPlayerStatus",
            "You already have another job! That should not happen 😅",
        ));
    }

    // Open a transaction
    let transaction = dynamodb().transact_write_items();

    let (transaction, yak) = if let Some(from_place) = from_place {
        // Buying a Yak is only possible if the game is not RESET
        if matches!(game_status, Some(GameStatus::Reset)) {
            return Err(trace_appsync_error!(
                "InvalidGameStatus",
                "The game is reset"
            ));
        }
        let waiting_yak = yak.unwrap().ok_or_else(|| {
            trace_appsync_error!("NoMoreYak", format!("No more yaks in the {from_place}"))
        })?;
        // Transactionally move the Yak in DynamoDB
        // Delete it from its old location
        let transaction = transaction.transact_items(
            waiting_yak
                .transact_delete()
                .condition(WaitingYak::item_exists())
                .build(),
        );

        (transaction, waiting_yak.into())
    } else {
        // Creating a Yak is only possible if the game is started
        if !matches!(game_status, Some(GameStatus::Started)) {
            return Err(trace_appsync_error!(
                "InvalidGameStatus",
                "The game is not started",
            ));
        }
        (transaction, Yak::new())
    };

    let game_status = game_status.ok_or_else(|| internal_server_error("Missing GameStatus"))?;
    let game_counts = game_counts.ok_or_else(|| internal_server_error("Missing GameCounts"))?;
    // Verify the buy if possible
    if let Some(from_place) = from_place {
        if game_counts[from_place] == 0 {
            return Err(trace_appsync_error!(
                "NoMoreYak",
                format!("No more yak in the {from_place}")
            ));
        }
    } else {
        if game_counts.total_yaks() >= game_counts.desired_yak_count() {
            return Err(trace_appsync_error!(
                "NoMoreYak",
                "No more yak in the nursery! Wait a little...",
            ));
        }
    }

    // Mut the player object with the new infos
    current_span.record("yak_id", yak.id.to_string());
    player.start_new_assignment(for_job, yak, game_counts.job_fees());

    // Get a transaction
    let transaction =
        transaction.transact_items(player.transact_start_assignment_with_secret_check(secret));

    // Execute the transaction
    if let Err(e) = transaction.send().await {
        use aws_sdk_dynamodb::operation::transact_write_items::TransactWriteItemsError;
        let error = e.into_service_error();
        // If the Error is a ConditionalCheckFail for the deletion part of the Yak move
        // then it means a race condition happened between 2 players trying to buy the same yak.
        // It should happen fairly often, so we return a nicer message.
        return Err(
            if let Some(from_place) = from_place
                && let TransactWriteItemsError::TransactionCanceledException(
                    transaction_canceled_exception,
                ) = &error
            {
                match transaction_canceled_exception.cancellation_reasons()[0].code() {
                    Some("ConditionalCheckFailed") => trace_appsync_error!(
                        "NoMoreYak",
                        format!("No more yak in the {from_place}")
                    ),
                    Some("TransactionConflict") => trace_appsync_error!(
                        "YakAlreadyTaken",
                        format!("Somebody else got the yak for {from_place}")
                    ),
                    _ => internal_server_error(error),
                }
            } else {
                internal_server_error(error)
            },
        );
    }

    // Update the GameCounts object
    // INFO: This is not part of the transaction because this particular
    // item is common to all players. So with 250 concurrent players,
    // putting it in the transaction would create *A LOT* of TransactionCanceledException
    let game_counts = game_counts
        .update_move_yak_with_sample_tracking(
            dynamodb(),
            from_place.map(GameCountIndex::WaitingPlace),
            Some(GameCountIndex::Job(for_job)),
        )
        .await?;

    Ok(GameUpdate::from(player)
        .with_game_status(game_status)
        .with_game_counts_derived(game_counts))
}

pub async fn sell_yak(
    player_id: ID,
    secret: String,
    yak_id: ID,
    from_job: Job,
    to_place: Option<WaitingPlace>,
) -> Result<GameUpdate, AppsyncError> {
    let current_span = tracing::Span::current();
    current_span.record("job", from_job.to_string());

    let (player, game_counts) = tokio::try_join!(
        Player::get(dynamodb(), ItemId::pk(player_id)).send(),
        GameCounts::get(dynamodb(), ItemId::NONE).send(),
    )
    .map_err(internal_server_error)?;

    let mut player =
        player.ok_or_else(|| trace_appsync_error!("PlayerNotFound", "Player does not exist"))?;
    current_span.record("player_name", &player.name);

    let game_counts = game_counts.ok_or_else(|| internal_server_error("Missing GameCounts"))?;

    // A player that do not have a job or not the corerct one should never be able to sell a Yak
    match &player.assignment {
        None => {
            return Err(trace_appsync_error!(
                "InvalidPlayerStatus",
                "You don't have a job! How would you get a Yak anyway? That should not happen 😅",
            ));
        }
        Some(assignment) => {
            if assignment.job != from_job {
                return Err(trace_appsync_error!(
                    "InvalidPlayerStatus",
                    format!("You are a {}, not a {from_job}!", assignment.job),
                ));
            }
            if assignment.yak_id() != yak_id {
                return Err(trace_appsync_error!(
                    "YakNotFound",
                    "The Yak does not exist"
                ));
            }
        }
    }

    // Get a transaction
    let transaction = dynamodb().transact_write_items();

    // Create the transaction
    let transaction =
        transaction.transact_items(player.transact_finish_assignment_with_secret_check(secret));

    // Mut the player object with the new infos
    let yak = player.finish_assignment();
    let waiting_yak = to_place.map(|to_place| WaitingYak::new(yak, to_place));

    let transaction = if let Some(waiting_yak) = waiting_yak {
        // Add it in the new location
        let transaction = transaction.transact_items(
            waiting_yak
                .transact_put()
                .condition(WaitingYak::item_not_exists())
                .build(),
        );
        transaction
    } else {
        // No destination place, the Yak is sheared (just deleted in other words)
        transaction
    };

    // Execute the transaction
    transaction.send().await.map_err(internal_server_error)?;

    // Update the GameCounts object
    // INFO: This is not part of the transaction because this particular
    // item is common to all players. Meaning putting it in the transaction
    // would create *A LOT* of TransactionCanceledException
    let game_counts = game_counts
        .update_move_yak_with_sample_tracking(
            dynamodb(),
            Some(GameCountIndex::Job(from_job)),
            to_place.map(GameCountIndex::WaitingPlace),
        )
        .await?;

    Ok(GameUpdate::from(player).with_game_counts_derived(game_counts))
}
