// dynamodb-facade is a project-specific wrapper around the AWS DynamoDB SDK.
// It provides trait-based abstractions (DynamoDBItem, DynamoDBItemOp, etc.)
// for type-safe single-table DynamoDB operations.
use dynamodb_facade::{
    Condition, DefaultMonoTable, DynamoDBItem, DynamoDBItemBatchOp, DynamoDBItemOp,
    DynamoDBItemTransactOp, ItemId, TableDefinition, Update, batch_delete, dynamodb_batch_write,
    dynamodb_execute_scan,
};
// AppsyncError: the error type returned to AppSync clients.
// ID: newtype wrapper for GraphQL ID scalars (UUID-based).
// appsync_operation: macro that registers a function as a handler for a specific GraphQL operation.
use lambda_appsync::{AppsyncError, ID, appsync_operation};

// dynamodb(): the instrumented client accessor generated by make_lambda_runtime! in main.rs.
// game_engine::*: all game domain types generated from the GraphQL schema.
// trace_appsync_error!: macro from error_utils.rs that creates errors and sets X-Ray error status.
use crate::{dynamodb, error_utils::internal_server_error, game_engine::*, trace_appsync_error};

// #[tracing::instrument(ret)] auto-creates a tracing span for this function call and logs the return value.
// #[appsync_operation(query(gameState))] registers this as the handler for the `gameState` GraphQL query.
// The make_handlers! macro routes Query.gameState events here.
#[tracing::instrument(ret)]
#[appsync_operation(query(gameState))]
pub async fn game_state() -> Result<GameState, AppsyncError> {
    // Full table scan: retrieves ALL items. In single-table design, different entity types
    // are distinguished by their _TYPE attribute (read via item_type()).
    let items =
        dynamodb_execute_scan(dynamodb().scan().table_name(DefaultMonoTable::table_name())).await?;
    let mut game_state = GameState::default();
    for item in items {
        // Pattern match on the entity type to deserialize each item into its proper Rust struct.
        // from_item() is provided by the DynamoDBItem trait from dynamodb-facade.
        match item.item_type() {
            Player::TYPE => game_state.players.push(Player::from_item(item)),
            GameStatus::TYPE => game_state.game_status = GameStatus::from_item(item),
            GameCounts::TYPE => {
                let game_counts = GameCounts::from_item(item);
                // into() calls From<GameCounts> implementations that convert
                // internal game counts into the GraphQL-facing types
                game_state.job_fees = game_counts.into();
                game_state.yak_counts = game_counts.into();
            }
            // Catch-all: log and ignore unrecognized types (forward compatibility)
            unknown_type => tracing::debug!(message = "Skipping item", ?unknown_type),
        }
    }
    Ok(game_state)
}

#[tracing::instrument(ret)]
#[appsync_operation(mutation(startGame))]
pub async fn start_game() -> Result<GameUpdate, AppsyncError> {
    // tokio::try_join! runs multiple async operations CONCURRENTLY (not sequentially)
    // and returns when all complete or any fails.
    let (game_counts, game_status) = tokio::try_join!(
        async {
            // ItemId::NONE: GameCounts is a singleton item (no primary key needed)
            let game_count = GameCounts::get(dynamodb(), ItemId::NONE)
                .send()
                .await?
                .map(|game_count| game_count.reset())
                .unwrap_or_else(|| GameCounts::with_player_count(0));
            game_count.put(dynamodb()).send().await?;
            Ok(game_count)
        },
        // Conditional put: only transitions to Started if current status is Reset or absent.
        // Prevents invalid state transitions. Uses DynamoDB conditional expressions.
        GameStatus::Started.put_if_current_status_compatible(dynamodb())
    )?;

    // Builder pattern: constructs a GameUpdate from the status, marks it as sampled
    // (triggers subscription delivery to players), and derives yak counts/fees from game counts.
    Ok(GameUpdate::from(game_status)
        .sampled()
        .with_game_counts_derived(game_counts))
}

#[tracing::instrument(ret)]
#[appsync_operation(mutation(stopGame))]
pub async fn stop_game() -> Result<GameUpdate, AppsyncError> {
    let (game_counts, game_status) = tokio::try_join!(
        GameCounts::with_updated_desired_yak_count(dynamodb(), 0),
        async {
            Ok(GameStatus::Stopped
                .put_if_current_status_compatible(dynamodb())
                .await?)
        }
    )?;
    Ok(GameUpdate::from(game_status)
        .sampled()
        .with_game_counts_derived(game_counts))
}

#[tracing::instrument(ret)]
#[appsync_operation(mutation(resetGame))]
pub async fn reset_game() -> Result<GameUpdate, AppsyncError> {
    // Start by setting the Reset state so players can no longer interact with the game
    let game_status = GameStatus::Reset
        .put_if_current_status_compatible(dynamodb())
        .await?;
    let mut game_update = GameUpdate::from(game_status).sampled();

    let items =
        dynamodb_execute_scan(dynamodb().scan().table_name(DefaultMonoTable::table_name())).await?;

    let batch_write_requests = items
        .into_iter()
        .filter_map(|item| match item.item_type() {
            Player::TYPE => Some(PlayerWithSecret::from_item(item).reset().batch_put()),
            Yak::TYPE => Some(batch_delete(item.into_key_only::<DefaultMonoTable>())),
            GameCounts::TYPE => {
                let game_counts = GameCounts::from_item(item).reset();
                game_update.job_fees = Some(game_counts.into());
                game_update.yak_counts = Some(game_counts.into());
                Some(game_counts.batch_put())
            }
            GameStatus::TYPE => None,
            unknown_item_type => {
                tracing::debug!(message = "Skipping item", ?unknown_item_type);
                None
            }
        })
        .collect::<Vec<_>>();
    dynamodb_batch_write(
        dynamodb(),
        DefaultMonoTable::table_name(),
        batch_write_requests,
    )
    .await?;

    Ok(game_update)
}

// fields(%player_id): records player_id as a Display-formatted span field (searchable in X-Ray).
#[tracing::instrument(ret, fields(%player_id))]
#[appsync_operation(mutation(removePlayer))]
pub async fn remove_player(player_id: ID) -> Result<Player, AppsyncError> {
    let player = Player::get(dynamodb(), ItemId::pk(player_id))
        .send()
        .await?
        // ok_or_else converts Option<Player> to Result. trace_appsync_error! also sets X-Ray error status.
        .ok_or_else(|| trace_appsync_error!("PlayerNotFound", "Player does not exist"))?;

    // DynamoDB Transaction: atomically deletes the player AND decrements the player count.
    // Both succeed or both fail — prevents inconsistency.
    dynamodb()
        .transact_write_items()
        .transact_items(
            Player::transact_delete_by_id(ItemId::pk(player_id))
                // Condition: only delete if the item exists (prevents double-deletion)
                .condition(Player::item_exists())
                .build(),
        )
        .transact_items(
            // Atomically decrement the counter with a guard preventing it from going negative
            GameCounts::transact_update_by_id(ItemId::NONE, Update::decrement("player_count", 1))
                .condition(GameCounts::item_exists() & Condition::gt("player_count", 0))
                .build(),
        )
        .send()
        .await?;

    Ok(player)
}

// skip(secret): excludes the secret from trace logs (security).
// fields(player_id): declares a span field that will be filled later with .record().
#[tracing::instrument(ret, skip(secret), fields(player_id, player_name=%name))]
#[appsync_operation(mutation(registerNewPlayer))]
pub async fn register_new_player(name: String, secret: String) -> Result<GameUpdate, AppsyncError> {
    let new_player_with_secret = PlayerWithSecret::new(name, secret);
    // Conditional put: only creates the player if no item with this PK/SK exists (UUID collision protection)
    new_player_with_secret
        .put(dynamodb())
        .condition(PlayerWithSecret::item_not_exists())
        .send()
        .await?;

    // Async closure used as a rollback function: if incrementing the game count fails,
    // this deletes the just-created player to maintain consistency.
    let try_rollback_player_creation = async |error| {
        let _ = new_player_with_secret
            .delete(dynamodb())
            .send()
            .await
            // Warn and supress the error
            .map_err(|error| {
                tracing::warn!(message = "Could not rollback player creation", ?error);
            })
            .ok();
        internal_server_error(error)
    };
    // Local `use` import (Rust idiom): brings Error into scope as DynamoError for pattern matching
    use aws_sdk_dynamodb::Error as DynamoError;
    // Local helper function defined inside another function (Rust allows this)
    async fn increment_gamecount() -> Result<GameCounts, DynamoError> {
        GameCounts::update_by_id(
            dynamodb(),
            ItemId::NONE,
            Update::increment("player_count", 1),
        )
        .condition(GameCounts::item_exists())
        .send()
        .await
    }

    // Multi-level error recovery for concurrent player registrations:
    // 1. Try incrementing player_count
    // 2. If item doesn't exist (first player), try creating it
    // 3. On race condition (another player created it simultaneously), retry increment
    // 4. On any other error, rollback the player creation
    let game_counts = match increment_gamecount().await {
        Ok(game_counts) => game_counts,
        // This is the first time we use the GameCount, need to create it
        Err(DynamoError::ConditionalCheckFailedException(_)) => {
            let game_counts = GameCounts::with_player_count(1);
            match game_counts
                .put(dynamodb())
                .condition(GameCounts::item_not_exists())
                .send()
                .await
            {
                Ok(_) => game_counts,
                Err(error) => {
                    match error {
                        // Race condition, multiple player attempted to create the GameCount
                        // Just retry the regular creation
                        DynamoError::ConditionalCheckFailedException(_) => {
                            match increment_gamecount().await {
                                Ok(game_counts) => game_counts,
                                Err(error) => {
                                    // Ok this time I'm out of options
                                    tracing::error!(
                                        message = "Could not create GameCounts",
                                        ?error
                                    );
                                    return Err(try_rollback_player_creation(error).await);
                                }
                            }
                        }
                        _ => {
                            tracing::error!(message = "Could not create GameCounts", ?error);
                            return Err(try_rollback_player_creation(error).await);
                        }
                    }
                }
            }
        }
        Err(error) => {
            tracing::error!(message = "Could not increment GameCounts", ?error);
            return Err(try_rollback_player_creation(error).await);
        }
    };
    let new_player = new_player_with_secret.into_player();
    // Fill the span field declared earlier (player_id was unknown until the player was created)
    tracing::Span::current().record("player_id", new_player.id.to_string());

    Ok(GameUpdate::from(new_player)
        .sampled()
        .with_game_counts_derived(game_counts))
}

